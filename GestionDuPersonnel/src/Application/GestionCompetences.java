    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JFileChooser dialogue = new JFileChooser();
        // affichage
        
        dialogue.showOpenDialog(null);
        
        dialogue.setFileFilter(new FileFilter() {
            @Override
            public boolean accept(File f) {
                
                if(f.isDirectory())
                    return true;
                else {
                    if(f.getName().equalsIgnoreCase("liste_competences.csv"))
                        return true;
                    else
                        return false;
                }
            }
            @Override
            public String getDescription(){
                return "Le fichier ne correspond pas!";//To change body of generated methods, choose Tools | Templates.
            }
        });
        
        // récupération du fichier sélectionné
        //System.out.println("Fichier choisi : " + dialogue.getSelectedFile());
        
        
        
        if (dialogue.getSelectedFile() != null){
            //chargement du fichier
            GestionSociete.fcc = new FileCsvCompetences(dialogue.getSelectedFile().toString());

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            for(String line : GestionSociete.fcc.data){

                    String[] data = line.split( new Character(GestionSociete.fcc.separator()).toString());
                    GestionSociete.competences.put(data[0],new Competence(data));
                    model.addRow(new Object[] {data[0],data[1],data[2]});

            }        
        }
    }//GEN-LAST:event_jButton2ActionPerformed

