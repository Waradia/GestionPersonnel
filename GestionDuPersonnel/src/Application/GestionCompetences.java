    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        int validerSuppr = JOptionPane.showConfirmDialog(null, "Voulez-Vous supprimer la competence N° "+ jTable1.getValueAt(jTable1.getSelectedRow(),0), "Attention", JOptionPane.YES_NO_OPTION);
        if (validerSuppr == 0){
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            GestionSociete.competences.remove(jTable1.getValueAt(jTable1.getSelectedRow(),0).toString());
            model.removeRow(jTable1.getSelectedRow());
            jTable1.setModel(model);
            jButton7.setVisible(false);
            jButton8.setVisible(false);
            jButton4.setVisible(false);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

