    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
         if ( nom.getText().isEmpty() || prenom.getText().isEmpty() || dateEntree.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Des Champs sont vides ", "Attention", JOptionPane.ERROR_MESSAGE);
        }
        
        else{
            GestionSociete.personnel.remove(GestionSociete.rowID);
            Employe e = new Employe(new String[]{prenom.getText(),nom.getText(),dateEntree.getText()});
            TableModel tm = jTable1.getModel();
            for (int i = 0; i < tm.getRowCount(); i++) {
                if ((Boolean)tm.getValueAt(i, 3))
                    e.ajouterCompetence(GestionSociete.competences.get(tm.getValueAt(i, 0).toString()));
            }
            GestionSociete.personnel.put(e.getId(), e);
            new GestionPersonnel().setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

